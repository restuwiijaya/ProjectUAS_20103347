    private void cdmsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cdmsimpanActionPerformed
        String NIDN = this.txNIDN.getText();
        String NAMA = this.txDS.getText();
        String JNS_K = "L";
       if(this.txJKP.isSelected()){
           JNS_K="P";
       }
        String GELAR = this.txGL.getText();
        String SQLInsert = "INSERT INTO ds (NIDN, NAMA, JK, GELAR) VALUES('"+NIDN+"','"+NAMA+"','"+JNS_K+"','"+GELAR+"');";
        if(this.UpdateData(SQLInsert)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data Ditambahkan");
        }
        this.cdmtambah.setEnabled(false);
        this.cdmbatal.setVisible(false);
        this.cdmtutup.setEnabled(true);
        this.cdmtambah.setEnabled(true);
    }//GEN-LAST:event_cdmsimpanActionPerformed

    private void cdmubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cdmubahActionPerformed
        String NIDN = this.txNIDN.getText();
        String NAMA = this.txDS.getText();
        String JNS_K = "L";
        if(this.txJKP.isSelected()){
            JNS_K="P";
        }
        String GELAR = this.txGL.getText();
        String SQLUpdate = "UPDATE ds SET NAMA='"+NAMA+"',JK='"+JNS_K+"',GELAR='"+GELAR+"'WHERE NIDN='"+NIDN+"';";
        if(this.UpdateData(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data Terupdate");
        }
    }//GEN-LAST:event_cdmubahActionPerformed

    private void cdmhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cdmhapusActionPerformed
        String NIDN = this.txNIDN.getText();
        String NAMA = this.txDS.getText();

        int opsi = JOptionPane.showConfirmDialog(null, "Yakin akan menghapus data "+NAMA+"("+NIDN+")?","Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete = "DELETE FROM ds WHERE NIDN='"+NIDN+"';";
            if(this.UpdateData(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            }
        }
    }//GEN-LAST:event_cdmhapusActionPerformed

