    private void getdata(){
        String sql = "Select NIDN, NAMA, JK, GELAR FROM ds;";
    model.getDataVector().removeAllElements();
    model.fireTableDataChanged();
    try{
        this.tampildata(sql);
        while(this.rs.next()){
            Object[] obj = new Object[4];
            obj[0]= this.rs.getString("NIDN");
            obj[1]= this.rs.getString("NAMA");
            String txJK = this.rs.getString("JK");
            if(txJK.equals("L")){
                obj[2]="Laki-Laki";
            }else{
                obj[2]="Perempuan";
            }
            obj[3]= this.rs.getString("GELAR");
        model.addRow(obj);
        }
    }catch(Exception e){
    }
    }
    private boolean UpdateData(String SQL){
        boolean hsl = false;
        try{
            Statement stmt= cnn.koneksi().createStatement();
            stmt.executeUpdate(SQL);
            hsl = true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Terjadi masalah pada intruksi SQL");
        }
        return hsl;
    }    
