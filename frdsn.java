    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        String urlGambarNull = "src/img/gambar.jpg";
        this.txNIDN.setText(model.getValueAt(tb.getSelectedRow(),0).toString());
        this.txDS.setText(model.getValueAt(tb.getSelectedRow(),1).toString());
        String txJK = model.getValueAt(tb.getSelectedRow(),2).toString();
        if(txJK.equals("Perempuan")){
            this.txJKP.setSelected(true);
        }else{
            this.txJKP.setSelected(true);
        }
        this.txGL.setText(model.getValueAt(tb.getSelectedRow(),3).toString());
        String urlGambar = "src/img/"+this.txNIDN.getText()+".jpg";
        BufferedImage loadImg = img.loadImage(urlGambar);
        if(loadImg == null ){
            loadImg = img.loadImage(urlGambarNull);
        }
        ImageIcon imageIcon = new ImageIcon(loadImg);
        this.imgphoto.setIcon(imageIcon);

        this.lbCRUDds.setText("Edit Data"+this.txNIDN.getText());
        this.cdmsimpan.setEnabled(false);
        this.cdmubah.setEnabled(true);
        this.cdmhapus.setEnabled(true);
    }//GEN-LAST:event_tbMouseClicked

