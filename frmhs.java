    private void getdata() {
       String sql = "Select NIM, NAMA, JURUSAN, TGL_LAHIR,JK FROM mhs;";
      model.getDataVector().removeAllElements();
      model.fireTableDataChanged();
    try{
        this.tampildata(sql);
        while(this.rs.next()){
            Object[] obj =new Object[5];
            obj[0] = this.rs.getString("NIM");
            obj[1] = this.rs.getString("NAMA");
            obj[2] = this.rs.getString("JURUSAN");
            obj[3] = this.rs.getString("TGL_LAHIR");
            obj[4] = this.rs.getString("JK");
            
            model.addRow(obj);
        }
    }catch(Exception e){
    }
    }
private void tampildata(String sql){
        try{
            Statement stmt = cnn.koneksi().createStatement();
            this.rs = stmt.executeQuery(sql);
        }catch(Exception e){
            System.out.println("Terjadi masalah pada query");
        }
    }
    private void initTable() {
        model = new DefaultTableModel();
        dt_mhs.setModel(model);
        model.addColumn("NIM");
        model.addColumn("NAMA");
        model.addColumn("JURUSAN");
         model.addColumn("TGL LAHIR");
        model.addColumn("JENIS KELAMIN");  
    }
